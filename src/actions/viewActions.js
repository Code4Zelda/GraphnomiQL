import * as actionTypes from '../constants/actionTypes';

export const selectNode = (currentNodeId) => {
  return {
    type: actionTypes.SELECT_NODE,
    payload: currentNodeId,
  };
};

export const selectEdge = (currentEdgeId) => {
  return {
    type: actionTypes.SELECT_EDGE,
    payload: currentEdgeId,
  };
};

export const clearSelection = () => {
  return {
    type: actionTypes.CLEAR_SELECTION,
  };
};

export const focusElement = (elementId) => {
  return {
    type: actionTypes.FOCUS_ELEMENT,
    payload: elementId,
  };
};


export const focusElementDone = (elementId) => {
  return {
    type: actionTypes.FOCUS_ELEMENT_DONE,
    payload: elementId,
  };
};
